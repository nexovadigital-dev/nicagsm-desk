const AudioCtx = typeof AudioContext !== 'undefined' ? AudioContext
    : (typeof webkitAudioContext !== 'undefined' ? webkitAudioContext : null);

function playSound(type) {
    if (!AudioCtx) return;
    try {
        const ctx  = new AudioCtx();
        const gain = ctx.createGain();
        gain.connect(ctx.destination);

        if (type === 'send') {
            // Tick corto y suave (250ms)
            const osc = ctx.createOscillator();
            osc.connect(gain);
            osc.type = 'sine';
            osc.frequency.setValueAtTime(880, ctx.currentTime);
            osc.frequency.exponentialRampToValueAtTime(660, ctx.currentTime + 0.12);
            gain.gain.setValueAtTime(0.15, ctx.currentTime);
            gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + 0.25);
            osc.start(ctx.currentTime);
            osc.stop(ctx.currentTime + 0.25);
        } else if (type === 'receive') {
            // Doble tono amigable (ding-dong)
            [0, 0.15].forEach((delay, i) => {
                const osc = ctx.createOscillator();
                osc.connect(gain);
                osc.type = 'sine';
                osc.frequency.setValueAtTime(i === 0 ? 1046 : 880, ctx.currentTime + delay);
                gain.gain.setValueAtTime(0.18, ctx.currentTime + delay);
                gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + delay + 0.35);
                osc.start(ctx.currentTime + delay);
                osc.stop(ctx.currentTime + delay + 0.35);
            });
        }

        // Cerrar contexto después de uso
        setTimeout(() => ctx.close(), 1000);
    } catch { /* silent */ }
}

