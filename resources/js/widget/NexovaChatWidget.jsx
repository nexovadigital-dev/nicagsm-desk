// Build a proper URL from user-entered value
const formatSocialUrl = (type, url) => {
    if (!url) return '#';
    const t = (type || '').toLowerCase();
    if (t === 'whatsapp') {
        if (/^\+?[\d\s\-()]+$/.test(url.trim())) {
            return `https://wa.me/${url.replace(/\D/g, '')}`;
        }
        return url.startsWith('http') ? url : `https://wa.me/${url.replace(/\D/g, '')}`;
    }
    if (t === 'telegram') {
        if (url.startsWith('@')) return `https://t.me/${url.slice(1)}`;
        if (!url.startsWith('http')) return `https://t.me/${url}`;
    }
    if (t === 'email' && !url.startsWith('mailto:') && !url.startsWith('http')) {
        return `mailto:${url}`;
    }
    if (t === 'phone' && !url.startsWith('tel:') && !url.startsWith('http')) {
        return `tel:${url}`;
    }
    return url;
};

