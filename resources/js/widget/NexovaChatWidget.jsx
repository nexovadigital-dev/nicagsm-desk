// Formulario pre-chat (Chatway-style)
// ---------------------------------------------------------------------------
function PreChatForm({ fields, onSubmit, accentColor, botName, welcomeMessage, botAvatar }) {
    const [values,  setValues]  = useState({});
    const [errors,  setErrors]  = useState({});
    const [loading, setLoading] = useState(false);

    const initial = (botName || 'N')[0].toUpperCase();

    // Derive a stable key from the field
    const fieldKey = (f, i) => f.name || f.label?.toLowerCase().replace(/[^a-z0-9]/gi, '_') || `field_${i}`;

    const validate = () => {
        const errs = {};
        (fields || []).forEach((f, i) => {
            const k = fieldKey(f, i);
            if (f.required && !String(values[k] || '').trim()) errs[k] = `${f.label || 'Campo'} es requerido`;
            if (f.type === 'email' && values[k] && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(values[k]))
                errs[k] = 'Email inválido';
        });
        return errs;
    };

    const handleSubmit = async e => {
        e.preventDefault();
        const errs = validate();
        if (Object.keys(errs).length) { setErrors(errs); return; }
        setLoading(true);
        // Build named data object so initSession can extract client_name, client_email, etc.
        const data = {};
        (fields || []).forEach((f, i) => {
            const k = fieldKey(f, i);
            data[k] = values[k] || '';
        });
        await onSubmit(data);
        setLoading(false);
    };

    const inputStyle = (k) => ({
        width: '100%', fontSize: 13.5, boxSizing: 'border-box',
        border: `1.5px solid ${errors[k] ? '#ef4444' : '#e5e7eb'}`,
        borderRadius: 9, padding: '10px 13px', outline: 'none',
        fontFamily: 'inherit', background: '#fff', color: '#1f2937',
        transition: 'border-color .12s',
    });

    return (
        <div style={{ flex: 1, display: 'flex', flexDirection: 'column', overflowY: 'auto',
            animation: 'nx-fade-up .2s ease-out' }}>

            {/* Welcome message strip */}
            <div style={{ background: accentColor, padding: '14px 18px 16px', color: '#fff', flexShrink: 0 }}>
                <p style={{ margin: 0, fontSize: 13, opacity: .92, lineHeight: 1.55 }}>
                    {welcomeMessage || '¡Hola! ¿En qué te puedo ayudar?'}
                </p>
            </div>

            <form onSubmit={handleSubmit}
                style={{ padding: '18px 16px', display: 'flex', flexDirection: 'column', gap: 12, flex: 1, background: '#f8fafc' }}>
                <p style={{ fontSize: 12, color: '#6b7280', margin: '0 0 2px', fontWeight: 500 }}>
                    Completa tus datos para continuar:
                </p>
                {(fields || []).map((f, i) => {
                    const k = fieldKey(f, i);
                    return (
                        <div key={k} style={{ display: 'flex', flexDirection: 'column', gap: 4 }}>
                            <label style={{ fontSize: 11.5, fontWeight: 600, color: '#374151' }}>
                                {f.label || 'Campo'}{f.required && <span style={{ color: '#ef4444' }}> *</span>}
                            </label>
                            {f.type === 'select' && f.options ? (
                                <select value={values[k] || ''} onChange={e => setValues(v => ({ ...v, [k]: e.target.value }))}
                                    style={{ ...inputStyle(k) }}>
                                    <option value="">Selecciona...</option>
                                    {f.options.map(o => <option key={o} value={o}>{o}</option>)}
                                </select>
                            ) : (
                                <input type={f.type || 'text'} value={values[k] || ''}
                                    onChange={e => setValues(v => ({ ...v, [k]: e.target.value }))}
                                    placeholder={f.placeholder || f.label}
                                    style={inputStyle(k)}
                                    onFocus={e => { e.target.style.borderColor = errors[k] ? '#ef4444' : accentColor; }}
                                    onBlur={e => { e.target.style.borderColor = errors[k] ? '#ef4444' : '#e5e7eb'; }} />
                            )}
                            {errors[k] && <span style={{ fontSize: 11, color: '#ef4444' }}>{errors[k]}</span>}
                        </div>
                    );
                })}
                <button type="submit" disabled={loading}
                    style={{ background: accentColor, color: '#fff', border: 'none', borderRadius: 9,
                        padding: '11px 0', fontSize: 13.5, fontWeight: 700, cursor: loading ? 'wait' : 'pointer',
                        fontFamily: 'inherit', marginTop: 4, display: 'flex', alignItems: 'center',
                        justifyContent: 'center', gap: 8, boxShadow: `0 2px 8px ${accentColor}55`,
                        opacity: loading ? .7 : 1, transition: 'opacity .15s' }}>
                    {loading && <IconSend spinning />}
                    Iniciar conversación →
                </button>
            </form>
        </div>
    );
}

